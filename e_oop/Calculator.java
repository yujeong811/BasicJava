package e_oop;

public class Calculator {
	
	//산술연산자 (+,-,*,/,%)
	//5개 메서드 
	
	long plus(long a, long b) {
		return a + b;
	}
	
	long minus(long div, long b) {
		return div - b;
	}
	
	long multi(long a, long b) {
		return a * b;
	}
	
	long div(long a, long b) {
		return a / b;
	}
	
	long nam(long a, long b) {
		return a % b;
	}
}
